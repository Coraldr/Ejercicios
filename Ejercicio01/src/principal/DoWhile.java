//Diseña un programa Java que muestre en pantalla los números enteros del 1 al 100.
//Mediante for
//Mediante while
//Mediante do-while 

public class DoWhile {

	public static void main(String[] args) {
		
		int i= 1;
		
		do {
			System.out.println(i);
			i++;
		} while (i<=100);

	}

}
